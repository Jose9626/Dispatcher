    private void ingresar(String pTipo, int pId, String pEstado, int contador, JTable tabla) {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        int indice = pTipo.lastIndexOf(".");
        pTipo = pTipo.substring(indice + 1);
        Object[] data = {pId, pTipo, pEstado, contador};
        modelo.addRow(data);
        tabla.setModel(modelo);
    }

    private int porcentaje(int progress, int end) {
        return (progress * 100) / end;
    }

    Timer timerReady = new Timer(0, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent ae) {
            LinkedList<Proceso> list = Dispatcher.unirColas();
            if (tblRunning.getRowCount() != 0) {
                DefaultTableModel modelo = (DefaultTableModel) tblRunning.getModel();
                modelo.setRowCount(0);
                tblRunning.setModel(modelo);
            }
            for (int i = 0; i < list.size(); i++) {
                ingresar(list.get(i).getClass().toString(), list.get(i).getPID(), list.get(i).getState().name(), list.get(i).getContador(), tblRunning);
            }
        }
    });

    Timer timerRunnable = new Timer(0, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent ae) {
            if (Dispatcher.process != null) {
                pIdField.setText(String.valueOf(Dispatcher.process.getPID()));
                try {
                    percentageField.setText(String.valueOf(porcentaje(Dispatcher.process.getContador(), Dispatcher.process.contadorFinal)));
                    progressBar.setValue(Integer.parseInt(percentageField.getText()));
                } catch (Exception e) {
                }
            } else {
                pIdField.setText("");
                percentageField.setText("");
                progressBar.setValue(0);
            }
        }
    });

    Timer timerTerminated = new Timer(0, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent ae) {
            LinkedList<Proceso> list = Dispatcher.terminados;
            if (tblTerminated.getRowCount() != 0) {
                DefaultTableModel modelo = (DefaultTableModel) tblTerminated.getModel();
                modelo.setRowCount(0);
                tblTerminated.setModel(modelo);
            }
            for (int i = 0; i < list.size(); i++) {
                ingresar(list.get(i).getClass().toString(), list.get(i).getPID(), list.get(i).getState().name(), list.get(i).getContador(), tblTerminated);
            }
        }
    });

