    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String process = cbProcesos.getSelectedItem().toString();
        String nombre = Proceso.class.getPackage().getName();
        Proceso proceso;
        try {
            proceso = (Proceso) Class.forName(nombre + "." + process).newInstance();
            Dispatcher.nuevos.enqueue(proceso);
            JOptionPane.showMessageDialog(this, "Se creó un proceso");
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException ex) {
            Logger.getLogger(DispatcherFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        Thread thread = new Thread(dispatcher);
        thread.start();
        timerReady.setDelay(1000);
        timerReady.start();
        timerRunnable.setDelay(1000);
        timerRunnable.start();
        timerTerminated.setDelay(1000);
        timerTerminated.start();
    }//GEN-LAST:event_btnIniciarActionPerformed

